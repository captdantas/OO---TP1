public class Cliente {
	String nome, endereco, telefone;
	
	public String getNome() {
		return nome;
	}
	public String getEndereco() {
		return endereco;
	}	
	public String getTelefone() {
		return telefone;
	}	
	public void setNome(String nomeAlterado) {
		this.nome = nomeAlterado;
	}
	public void setEndereco(String enderecoAlterado) {
		this.endereco = enderecoAlterado;
	}
	public void setTelefone(String telefoneAlterado) {
		this.telefone = telefoneAlterado;
	}
}
